/*

Solve the following problems:

12>=12	| NOT (12>=12)  |	1 AND 1			| (7=7) and (7>5)
12>7	| NOT (12<7)	|	True AND 0		| (7=7) and (7<5)
8<6		| NOT (8<6)	    |	0 OR 1			| (7=7) OR (7<5)
8=8		| NOT (8=8)	    |	0 OR 0			| (7<7) OR (7>5)
12<=12	| NOT (12<=12)  |	NOT 0			| NOT (7=7) and (7>5)
7=5		| NOT (7=5)	    |	NOT (1 OR 0)	| (7=7) and Not (7<5)

*/


#include <iostream>
using namespace std;

int main()
{


    cout << "12 >= 12       --> " << (12 >= 12) << "\t|";
    cout << "NOT(12 >= 12)  --> " << !(12 >= 12) << "\t|";
    cout << "1 AND 1        --> " << (1 && 1) << "\t|";
    cout << "(7==7 && 7>5)  --> " << ((7 == 7) && (7 > 5)) << endl;


    cout << "12 > 7        --> " << (12 > 7) << "\t|";
    cout << "NOT(12 < 7)   --> " << !(12 < 7) << "\t|";
    cout << "True AND 0    --> " << (1 && 0) << "\t|";
    cout << "(7==7 && 7<5) --> " << ((7 == 7) && (7 < 5)) << endl;


    cout << "8 < 6          --> " << (8 < 6) << "\t|";
    cout << "NOT(8 < 6)     --> " << !(8 < 6) << "\t|";
    cout << "0 OR 1         --> " << (0 || 1) << "\t|";
    cout << "(7==7 || 7<5)  --> " << ((7 == 7) || (7 < 5)) << endl;

    cout << "8 == 8        --> " << (8 == 8) << "\t|";
    cout << "NOT(8 == 8)   --> " << !(8 == 8) << "\t|";
    cout << "0 OR 0        --> " << (0 || 0) << "\t|";
    cout << "(7<7 || 7>5)  --> " << ((7 < 7) || (7 > 5)) << endl;


    cout << "12 <= 12         --> " << (12 <= 12) << "\t|";
    cout << "NOT(12<=12)      --> " << !(12 <= 12) << "\t|";
    cout << "NOT 0            --> " << !0 << "\t|";
    cout << "NOT(7==7 && 7>5) --> " << !((7 == 7) && (7 > 5)) << endl;


    cout << "7 == 5         --> " << (7 == 5) << "\t|";
    cout << "NOT(7 == 5)    --> " << !(7 == 5) << "\t|";
    cout << "NOT(1 OR 0)    --> " << !(1 || 0) << "\t|";
    cout << "(7==7&&!(7<5)) --> " << ((7 == 7) && !(7 < 5)) << endl;

    return 0;
}